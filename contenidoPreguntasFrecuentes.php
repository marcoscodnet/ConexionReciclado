                <p class="tituloNaranjaFaq" id="venta">Vendedores</p>
                <div class="lineaPunteada"></div>
                <p class="tituloNaranja">¿Cuál es el costo de publicación?</p>
                <p>Publicar no tiene costo alguno.</p>
                <p class="tituloNaranja">¿Cuál es el costo por conexión exitosa?</p>
                <p>En una primera etapa, el servicio es totalmente gratuito.</p>
                <p class="tituloNaranja">¿Cuanto duran las publicaciones?</p>
                <p>Las publicaciones duran 45 días. Una vez finalizado este periodo, y en caso de no haberse realizado una conexión exitoso, se le dará la posibilidad de republicar su residuo.</p>
                <p class="tituloNaranja">¿Que significa cuando un residuo tiene precio 0?</p>
                <p>Significa que el vendedor esta dispuesto a entregar su residuo gratis, sin ningún costo.</p>
                <p class="tituloNaranja">¿Es imprescindible completar toda la información sobre un residuo a vender?</p>
                <p>La información obligatoria esta marcada con un asterisco. Igual recuerde que cuanta mas información, mas posibilidades de conexión exitosa, y menos preguntas por nuestra parte y de los posibles compradores.</p>
                <p class="tituloNaranja">¿Que tipo de residuos están prohibidos de publicar en Conexión Reciclado?</p>
                <p>Productos considerados peligrosos según la ley 24.051 (poner el link http://www2.medioambiente.gov.ar/sian/pan/Leyes-decretos/Ley24051.htm) están totalmente prohibidos. Todas las publicaciones están sujetas a una revisión de 48hs por parte de Conexión Reciclado. Cualquier duda mirá nuestros términos y condiciones (link).</p>
                <p class="tituloNaranja">¿Cuándo puedo ver la información completa del comprador?</p>
                <p>Una vez realizada la oferta por parte del comprador, conectaremos a ambas partes con la información completa de cada uno.</p>
                <p class="tituloNaranja">¿Cuánto tiempo dispongo para realizar la calificación de la otra parte?</p>
                <p>Tenés hasta 45 días después de haberse realizado la conexión para calificar a la otra parte. En caso de que la transacción no se haya podido concretar, indícanos cual fue la razón.</p>
                <div class="lineaPunteada"></div>
                <p class="tituloNaranjaFaq" id="compra">Compradores</p>
                <div class="lineaPunteada"></div>
                <p class="tituloNaranja">¿Necesito estar registrado para ver los residuos en venta?</p>
                <p>No, no hace falta estar registrado. Cualquiera puede ver los residuos en venta.</p>
                <p class="tituloNaranja">¿Necesito estar registrado para realizar preguntas?</p>
                <p>Si, debes estar registrado en Conexión Reciclado para hacer preguntas a los vendedores sobre su residuo.<p>
                <p class="tituloNaranja">¿Que significa cuando un residuo tiene precio 0?</p>
                <p>Significa que el vendedor esta dispuesto a entregar su residuo gratis, sin ningún costo.<p>
                <p class="tituloNaranja">¿Esta incluido el costo de transporte?</p>
                <p>Generalmente el costo del transporte recae sobre el vendedor. Por esta razón, es importante tener en cuenta la ubicación geográfica del residuo.<p>
                <p class="tituloNaranja">¿Cuándo puedo ver la información completa del vendedor?</p>
                <p>Una vez que este seguro y decidido que quiere comprar el residuo en cuestión, debe hacer clic en comprar. Recuerde que una oferta es un compromiso de compra. Una vez realizada la transacción, conectaremos a ambas partes con la información completa de cada uno.<p>
                <p class="tituloNaranja">¿Cuánto tiempo dispongo para realizar la calificación de la otra parte?</p>
                <p>Tenés hasta 45 días después de haberse realizado la conexión para calificar a la otra parte. En caso de que la transacción no se haya podido concretar, indícanos cual fue la razón.<p>
